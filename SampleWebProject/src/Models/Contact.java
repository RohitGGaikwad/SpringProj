package Models;

public class Contact {
	public String Guardian_Name;
	public String Guardian_Phn_No;
	public String Guardian_Tel_No;
	public String Address;
	public String Email;
	public String getGuardian_Name() {
		return Guardian_Name;
	}
	public void setGuardian_Name(String guardian_Name) {
		Guardian_Name = guardian_Name;
	}
	public String getGuardian_Phn_No() {
		return Guardian_Phn_No;
	}
	public void setGuardian_Phn_No(String guardian_Phn_No) {
		Guardian_Phn_No = guardian_Phn_No;
	}
	public String getGuardian_Tel_No() {
		return Guardian_Tel_No;
	}
	public void setGuardian_Tel_No(String guardian_Tel_No) {
		Guardian_Tel_No = guardian_Tel_No;
	}
	public String getAddress() {
		return Address;
	}
	public void setAddress(String address) {
		Address = address;
	}
	public String getEmail() {
		return Email;
	}
	public void setEmail(String email) {
		Email = email;
	}
	
}
